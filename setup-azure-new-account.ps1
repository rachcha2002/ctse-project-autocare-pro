Write-Host "[4/4] Creating Gateway and Frontend (Port 80)..." -ForegroundColor Cyan

az containerapp create --name gateway --resource-group $RG_NAME --environment $ENV_NAME `
    --image rachcha/autocare-gateway:latest --target-port 80 --ingress external `
    --env-vars PORT="80" | Out-Null

az containerapp create --name frontend --resource-group $RG_NAME --environment $ENV_NAME `
    --image rachcha/autocare-frontend:latest --target-port 80 --ingress external | Out-Null


